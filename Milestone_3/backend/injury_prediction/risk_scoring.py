"""
risk_scoring.py
---------------
Calculates an overall injury risk score based on
predictions generated by predictor.py.
"""

from .predictor import InjuryPredictor


class RiskScoringEngine:

    def __init__(self, milestone2_output_folder):

        self.predictor = InjuryPredictor(milestone2_output_folder)

        self.predictions = self.predictor.predict()

        self.risk_points = {
            "Low": 10,
            "Moderate": 20,
            "High": 30,
            "Good": 5,
            "Average": 15,
            "Poor": 30
        }

    # -------------------------------------------------

    def calculate_total_score(self):

        score = 0

        for value in self.predictions.values():
            score += self.risk_points.get(value, 0)

        return score

    # -------------------------------------------------

    def risk_level(self, score):

        if score < 30:
            return "LOW"

        elif score < 60:
            return "MODERATE"

        elif score < 90:
            return "HIGH"

        return "CRITICAL"

    # -------------------------------------------------

    def generate_report(self):

        score = self.calculate_total_score()

        return {
            "Overall Score": score,
            "Overall Risk": self.risk_level(score),
            "Predictions": self.predictions
        }


if __name__ == "__main__":

    engine = RiskScoringEngine("Milestone 2/outputs")

    report = engine.generate_report()

    print("\n========== RISK REPORT ==========\n")

    for key, value in report.items():
        print(f"{key}: {value}")